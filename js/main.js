    var vm = this;
    vm.today = new Date();
    vm.minDate = vm.today.toISOString().slice(0, 10);
    vm.minDateTime = vm.today.toISOString();
    vm.nextSixWeeks = new Date(vm.today.getTime() + 7 * 6 * 24 * 60 * 60 * 1000).toISOString().slice(0, 10);
